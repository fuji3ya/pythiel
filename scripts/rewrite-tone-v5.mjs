// 第5弾：残った断定文を逐次ターゲットして潰す
import { readFileSync, writeFileSync } from 'fs';

const t = JSON.parse(readFileSync('lib/synthesis/corpus/tarot.v2.json', 'utf-8'));
const targetKeys = [];
for (let i = 0; i <= 21; i++) targetKeys.push(`major-${i}:false`, `major-${i}:true`);
targetKeys.push('pentacles-12:false', 'pentacles-12:true', 'swords-12:false', 'swords-12:true');

const softFields = ['now_implies', 'unread_truth', 'prediction_arc', 'turning_point', 'advice_principle', 'caveat'];

// 残った頻出パターンを直接ターゲット
const rules = [
  // 短い断定文末
  [/だけ。$/gm, 'だけのように見えます。'],
  [/もの。$/gm, 'もののように思います。'],
  [/状態。$/gm, '状態のように見えます。'],
  [/段階。$/gm, '段階のように見えます。'],
  [/位相。$/gm, '位相のように見えます。'],
  [/必要なもの。$/gm, '必要なものになるかもしれません。'],
  [/必要な選択。$/gm, '必要な選択になるかもしれません。'],
  // 文中の「○○。」
  [/きっかけ。/g, 'きっかけになるように思います。'],
  [/エネルギー。/g, 'エネルギーのように見えます。'],
  [/盲点がある。/g, '盲点があるのかもしれません。'],
  [/盲点。/g, '盲点のように見えます。'],
  [/ストーリー。/g, 'ストーリーのように見えます。'],
  [/合図。/g, '合図のように見えます。'],
  [/合図のように見えます/g, '合図のように見えます'],  // dedupe
  [/タイミング。/g, 'タイミングになるかもしれません。'],
  [/絶対値。/g, '絶対値のように見えます。'],
  // 「○○な日」「○○な時期」末尾
  [/な日。/g, 'な日のように見えます。'],
  [/な時期。/g, 'な時期のように見えます。'],
  [/な時。/g, 'な時のように見えます。'],
  [/な選択。/g, 'な選択のように思います。'],
  // 「○○の仕組み」
  [/仕組み。/g, '仕組みのように見えます。'],
  // 「○○の問い」
  [/今日の問い。/g, '今日の問いのように見えます。'],
  // 「○○の動き」
  [/動き。/g, '動きのように見えます。'],
  // 「○○変える」
  [/重心が変わる/g, '重心が少し変わる'],
  [/感覚が変わる/g, '感覚が少し変わる'],
  [/景色が変わる/g, '景色が少し変わる'],
  [/が変わる。/g, 'が少し変わるかもしれません。'],
  // 「○○の力」「○○の重み」
  [/力。/g, '力なのだと思います。'],
  [/重み。/g, '重みのように見えます。'],
  // 「○○の方向」
  [/への方向。/g, 'への方向のように見えます。'],
  // 「○○がある」「○○がない」末尾
  [/がある。/g, 'があるように見えます。'],
  [/がない。/g, 'がないように見えます。'],
  // 「○○ことが」型
  [/ことが今週の課題/g, 'ことが、今週の課題のように見える'],
  // 「○○の選択肢」
  [/2つの選択肢。/g, '2つの選択肢が見えるかもしれません。'],
  // 「○○の時間」
  [/時間。/g, '時間のように見えます。'],
  // 「○○の節目」
  [/節目。/g, '節目のように見えます。'],
  // 「○○の準備」
  [/準備。/g, '準備のように見えます。'],
  // 「○○の機会」
  [/機会。/g, '機会のように見えます。'],
  // 「○○の出来事」
  [/出来事。/g, '出来事になるかもしれません。'],
  // 「○○の答え」
  [/答え。/g, '答えのように見えます。'],
  // 命令文「○○せず」末尾
  [/と言わず/g, 'と言わずに'],
  // 「○○の根拠」
  [/根拠。/g, '根拠のように見えます。'],
  // 「○○のセンス」
  [/センス。/g, 'センスのように見えます。'],
  // 「○○の認識」
  [/認識。/g, '認識のように見えます。'],
  // 「○○の輪郭」
  [/輪郭。/g, '輪郭のように見えます。'],
  // 「○○の声」
  [/今日の声/g, '今日の声'],  // skip
  [/その声。/g, 'その声のように見えます。'],
  // 「○○のメッセージ」
  [/メッセージ。/g, 'メッセージのように見えます。'],
  // 「○○の判別」
  [/判別。/g, '判別のように見えます。'],
  // 「○○の調整」
  [/調整。/g, '調整のように見えます。'],
];

let totalChanges = 0;
for (const k of targetKeys) {
  if (!t[k]) continue;
  for (const f of softFields) {
    if (!t[k][f]) continue;
    const before = t[k][f];
    let after = before;
    for (const [pat, rep] of rules) {
      after = after.replace(pat, rep);
    }
    if (before !== after) {
      t[k][f] = after;
      totalChanges++;
    }
  }
}
writeFileSync('lib/synthesis/corpus/tarot.v2.json', JSON.stringify(t, null, 2), 'utf-8');
console.log(`Total v5: ${totalChanges}`);
