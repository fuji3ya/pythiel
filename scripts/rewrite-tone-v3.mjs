// 第3弾：より広い文末置換ルール
// 「。今日問うのは「○○」。」のような断定文末を寄り添いに
import { readFileSync, writeFileSync } from 'fs';

const t = JSON.parse(readFileSync('lib/synthesis/corpus/tarot.v2.json', 'utf-8'));
const targetKeys = [];
for (let i = 0; i <= 21; i++) targetKeys.push(`major-${i}:false`, `major-${i}:true`);
targetKeys.push('pentacles-12:false', 'pentacles-12:true', 'swords-12:false', 'swords-12:true');

const softFields = ['now_implies', 'unread_truth', 'prediction_arc', 'turning_point', 'advice_principle', 'caveat'];

// 第3弾ルール: 「今日問うのは『○○』」「最も賢い動き」など強い断定を柔らかく
const rules = [
  // 「今日問うのは『○○』」型
  [/今日問うのは「([^」]+)」。/g, '今日そっと問うてみるとしたら、「$1」なのかもしれません。'],
  [/今日問うべきは「([^」]+)」。/g, '今日問うてみるといいのかもしれないのは、「$1」のように思います。'],
  // 「最も○○な動き」「最も賢い○○」
  [/最も賢い動き。/g, 'もっとも賢い動きになるように見えます。'],
  [/最も賢い動き/g, 'もっとも賢い動きのように見える'],
  [/最大の力。/g, 'もっとも大きな力になるように思います。'],
  [/最大の課題。/g, 'もっとも大切な課題のように見えます。'],
  [/最も重要な情報。/g, 'もっとも大切な手がかりになるかもしれません。'],
  // 「○○の出口」「○○の入り口」
  [/出口。/g, '出口のように見えます。'],
  [/入り口。/g, '入り口のように思います。'],
  [/最初のドアを開ける。/g, '最初のドアを開けるように見えます。'],
  // 「○○と知っておく」
  [/と知っておく。/g, 'と知っておくといいのかもしれません。'],
  // 「○○の証拠」「○○の証明」
  [/の証拠。/g, 'の証拠なのだと思います。'],
  [/の証明。/g, 'の証明のように見えます。'],
  // 「○○が来る」「○○が訪れる」（既訪のものを除く）
  [/瞬間が来る。/g, '瞬間が訪れるように見えます。'],
  [/出来事が来る。/g, '出来事が訪れるかもしれません。'],
  [/直感が来る/g, '直感がそっと訪れる'],
  [/状況が動く。/g, '状況がそっと動き始めるように見えます。'],
  // 「○○が変わる」
  [/位置が変わる/g, '位置が変わるように見えます'],
  [/景色を決める/g, '景色を決めていくように見えます'],
  [/結果が出る/g, '結果が見えてくる'],
  [/結果を出す/g, '結果が出やすい'],
  // 「○○が分岐点」
  [/が分岐点。/g, 'が、分岐点のように見えます。'],
  [/が今日のテーマ/g, 'が今日のテーマのように見える'],
  // 「○○を選ぶ」「○○する権利がある」
  [/を選ぶ/g, 'を選んでみる'],
  // 「○○を観察する日」「○○を寝かせる」
  [/観察する日。/g, '観察してみる日のように思います。'],
  [/3日以上寝かせると/g, '3日以上寝かせてしまうと'],
  // 「○○が来る」一般
  [/が来る。/g, 'が訪れるかもしれません。'],
  // 「○○を実行する」
  [/1つ実行する。/g, '1つ、実行してみてください。'],
  [/今週中にする。/g, '今週中にしてみてください。'],
  // 「○○のために」「○○の方法」など
  [/唯一の方法。/g, '唯一の道なのかもしれません。'],
  // 「自分から動くか」など分岐型
  [/分岐に立つ。/g, '分岐に立つように見えます。'],
  [/反射で否定せず、5秒間受け取る。/g, '反射的に否定せず、5秒間そっと受け取ってみてください。'],
  // 「○○○○。○○○○。」のような複文末
  [/痛くなる可能性がある。/g, 'もっと痛くなる可能性があるように思います。'],
  // 「○○の儀式」
  [/儀式。/g, '儀式のように見えます。'],
  // 「○○の同一視には注意」
  [/同一視には注意。/g, '同一視には、注意してみてください。'],
  // 命令的「○○せず」「○○する」（caveatに多い）
  [/手放さなくていい/g, '手放さなくていいのかもしれません'],
  [/見逃さない/g, '見逃さずにいてください'],
  [/逃さない/g, '逃さずにいてください'],
  [/Foolは次の崖に立つ——/g, 'Foolは次の崖に立っているのだと思います——'],
  [/止めない——/g, '止めないでください——'],
  // 「○○することが」型
  [/向き合う最初の対話を。/g, 'そっと向き合ってみてください。'],
  [/最初の連絡」か「最初の申込み」を1つ実行する。/g, '最初の連絡」か「最初の申込み」を1つ、試してみてもいいのかもしれません。'],
  // 「○○の壁に最初のドアを開ける」
  [/最初の章/g, '最初の章'],  // skip
  // 「○○が言うこと」
  [/と言われる/g, 'と告げられる'],
  // ↑ ここの「最後の一手」「最後の一歩」
  [/最後の一歩は派手ではない——/g, '最後の一歩は派手ではないのだと思います——'],
];

let totalChanges = 0;
const changes = {};
for (const k of targetKeys) {
  if (!t[k]) continue;
  changes[k] = 0;
  for (const f of softFields) {
    if (!t[k][f]) continue;
    const before = t[k][f];
    let after = before;
    for (const [pat, rep] of rules) {
      after = after.replace(pat, rep);
    }
    if (before !== after) {
      t[k][f] = after;
      changes[k]++;
      totalChanges++;
    }
  }
}
writeFileSync('lib/synthesis/corpus/tarot.v2.json', JSON.stringify(t, null, 2), 'utf-8');
console.log(`Total v3 rewrites: ${totalChanges}`);
const sorted = Object.entries(changes).sort((a,b)=>b[1]-a[1]);
console.log('Top:', sorted.slice(0,8).map(([k,c])=>`${k}=${c}`).join(', '));
