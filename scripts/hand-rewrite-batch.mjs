const missed = [];
for (const [key, field, oldText, newText] of rewrites) {
  if (!t[key] || !t[key][field]) { missed.push(`${key}.${field} - missing`); continue; }
  if (t[key][field].includes(oldText.trim().slice(0, 40))) {
    t[key][field] = newText;
    totalEdits++;
  } else {
    missed.push(`${key}.${field} - text not found`);
  }
}

