// 第2弾：より強力な置換ルール
import { readFileSync, writeFileSync } from 'fs';

const t = JSON.parse(readFileSync('lib/synthesis/corpus/tarot.v2.json', 'utf-8'));

const targetKeys = [];
for (let i = 0; i <= 21; i++) targetKeys.push(`major-${i}:false`, `major-${i}:true`);
targetKeys.push('pentacles-12:false', 'pentacles-12:true', 'swords-12:false', 'swords-12:true');

const softFields = ['now_implies', 'unread_truth', 'prediction_arc', 'turning_point', 'advice_principle', 'caveat'];

// 第2弾ルール（softMarkerを増やすため、文末を柔らかく）
// 「〜こと。」「〜なる。」「〜ない。」「〜来る。」など断定的文末を、寄り添い文末に
const rules = [
  // 文末固定パターン
  [/と知っておく。/g, 'と知っておくといいのかもしれません。'],
  [/と分かっている。/g, 'と分かっているのかもしれません。'],
  [/と感じている。/g, 'と感じているのかもしれません。'],
  [/と気づいていない。/g, 'と、気づきにくいのかもしれません。'],
  [/ことに気づいていない。/g, 'ことに、気づきにくいのかもしれません。'],
  [/が問われている。/g, 'が、今問われているのかもしれません。'],
  [/が来る。/g, 'が訪れるかもしれません。'],
  [/出来事が来る/g, '出来事が訪れる'],
  [/瞬間が来る。/g, '瞬間が訪れるかもしれません。'],
  [/合図。/g, '合図のように見えます。'],
  [/問い直しの入り口。/g, '問い直しの入り口のように思います。'],
  [/入り口。/g, '入り口のように見えます。'],
  [/転換点。/g, '転換点のように見えます。'],
  [/答えが出る。/g, '答えが出るように見えます。'],
  [/結果を出す。/g, '結果を出すように見えます。'],
  [/正解。/g, '正解のように見えます。'],
  [/正解だ。/g, '正解のように思います。'],
  [/間違い。/g, '間違いのように見えます。'],
  [/から逃げている。/g, 'から逃げているのかもしれません。'],
  [/に飛び込んでいる。/g, 'に飛び込んでいるのかもしれません。'],
  [/まで蓋をしてきた。/g, 'まで蓋をしてきたのかもしれません。'],
  [/から始まる。/g, 'から始まるのだと思います。'],
  [/見えてくる。/g, '見えてくるように思います。'],
  [/動き始める。/g, '動き始めるかもしれません。'],
  [/動き出す。/g, '動き出すように思います。'],
  [/動かないこと。/g, '動かないでいたいところです。'],
  // 「〜する。」短いものを「〜してみては。」（advice系で多い）
  [/書き出す。/g, '書き出してみてください。'],
  [/書く。/g, '書いてみてください。'],
  [/答える。/g, '答えてみてください。'],
  [/口に出す。/g, '口に出してみてください。'],
  [/問う。/g, '問うてみてください。'],
  [/伝える。/g, '伝えてみてください。'],
  [/置く。/g, '置いてみてください。'],
  [/数える。/g, '数えてみてください。'],
  [/触れる。/g, '触れてみてください。'],
  [/作る。/g, '作ってみてください。'],
  [/決める。/g, '決めてみてください。'],
  [/与える。/g, '与えてみてください。'],
  [/シミュレーションする。/g, 'シミュレーションしてみてください。'],
  [/休眠させる。/g, '休眠させてみてください。'],
  [/解放する。/g, '解放してあげてください。'],
  [/承認する。/g, '承認してあげてください。'],
  [/休む」と決める。/g, '休む」と決めてみてください。'],
  [/「今月は休む」と決める/g, '「今月は休む」と決めてみる'],
  [/手を止めて書き留める。/g, '手を止めて書き留めてみてください。'],
  [/書き留める/g, '書き留めて'],  // ※注意：文中の「書き留めること」も影響
  // 「〜する」を別パターンで
  [/従う——/g, '従ってみる——'],
  [/逃さない。/g, '逃さずにいたいところです。'],
  [/しない。/g, 'しないでいるところがあります。'],
  // ありがちな断定形
  [/だと知っておくといいのかもしれません/g, 'だと知っておくといいのかもしれません'],  // 既存維持
  [/だ。$/gm, 'のかもしれません。'],  // 文末「だ。」改行前
  [/だ。/g, 'のかもしれません。'],  // 一般「だ。」
  [/である。/g, 'のように見えます。'],
];

let totalChanges = 0;
const changes = {};

for (const k of targetKeys) {
  if (!t[k]) continue;
  changes[k] = 0;
  for (const f of softFields) {
    if (!t[k][f]) continue;
    const before = t[k][f];
    let after = before;
    for (const [pat, rep] of rules) {
      after = after.replace(pat, rep);
    }
    if (before !== after) {
      t[k][f] = after;
      changes[k]++;
      totalChanges++;
    }
  }
}

writeFileSync('lib/synthesis/corpus/tarot.v2.json', JSON.stringify(t, null, 2), 'utf-8');
console.log(`総変更フィールド数 (v2): ${totalChanges}`);
const sorted = Object.entries(changes).sort((a, b) => b[1] - a[1]);
console.log('Top10:', sorted.slice(0, 10).map(([k, c]) => `${k}=${c}`).join(' / '));
const untouched = Object.keys(changes).filter(k => changes[k] === 0);
console.log(`変更なし: ${untouched.length}件`);
